  int16_t       hw_table_r[ISP_GAMMA_NUM_ENTRIES];
  int16_t       hw_table_g[ISP_GAMMA_NUM_ENTRIES];
  int16_t       hw_table_b[ISP_GAMMA_NUM_ENTRIES];
