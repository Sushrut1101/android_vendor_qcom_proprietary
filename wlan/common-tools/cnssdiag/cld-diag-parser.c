/*
 * Syntax for format-specifier is the following
 * %[parameter][flags][width][.precision][length]type
 *
 * We need states corresponding to all of the above optional and mandatory
 * fields coming after '%'
 */

#define IS_NUMERICAL_NOT_ZERO(c) ((c >= '1') && (c <= '9'))
#define IS_NUMERICAL(c) ((c >= '0') && (c <= '9'))
#define IS_MATCH(c, arr) (is_match(c, arr, sizeof(arr)))

enum format_state {
	FORMAT_NON_STATE, /* When not inside format specifier */
	FORMAT_START_STATE, /* Immediately after encountering the first '%' */
	FORMAT_PARAMETER_STATE, /* If we encounter parameter '$' */
	FORMAT_FLAG_STATE, /*If we encounter flags inside format-specifier */

	/*
	 * We need 2 states for width specifiers
	 * width specifiers are indicated by numerical characters.
	 * But encountering numerical characters can mean
	 * either of the following
	 * * Parameter indication (in case the numericals are followed by '$'
	 * * Width indication (here the numericals will not be followed by '$'
	 * When encountering numerical, initially go to WIDTH_STATE, and then
	 * go to the below possible states
	 * * FORMAT_PARAMETER_STATE - on encountering '$'
	 * * FORMAT_WIDTH_STATE_COFIRMED - if did not find '$'
	 */
	FORMAT_WIDTH_STATE,
	FORMAT_WIDTH_STATE_CONFIRMED,

	FORMAT_PRECISION_STATE, /* If encountered the precision character '.'*/
	FORMAT_LENGTH_STATE, /* If encountered the length characters */
	FORMAT_END_STATE, /* After we completely parse the format-specifier */
	FORMAT_ERROR_STATE /* Any non-conforming scenarios */
};

char format_flag_chars[] = {' ', '-', '+', '0', '#'};
char format_type_chars[] = {'a', 'c', 'd', 'e', 'f', 'g', 'i', 'm', 'o', 'p',
			    'u', 'x', 'A', 'C', 'E', 'F', 'G', 'X', '%'};
char format_len_chars[] = {'h', 'j', 'l', 'q', 't', 'z', 'L'};

static int is_match(char c, char *list_of_chars, int num_chars)
{
	int cnt;

	for (cnt = 0; cnt < num_chars; cnt++)
		if (c == list_of_chars[cnt])
			return 1;
	return 0;
}


static enum format_state
get_next_state_in_precision(enum format_state cur_state, char c)
{
	if (IS_NUMERICAL(c))
		return FORMAT_PRECISION_STATE;

	return FORMAT_ERROR_STATE;
}

static enum format_state
get_next_state_in_width(enum format_state cur_state, char c)
{
	if (c == '.')
		return FORMAT_PRECISION_STATE;
	else if (IS_NUMERICAL(c))
		return cur_state;
	else if (c == '$') {
		if (cur_state == FORMAT_WIDTH_STATE_CONFIRMED) {
			diag_printf_wrapper(
			"%s: ERROR!! Encountered $ in CONFIRMED state\n",
			__func__);
			return FORMAT_ERROR_STATE;
		} else {
			return FORMAT_PARAMETER_STATE;
		}
	}
	diag_printf_wrapper(
		"%s: ERROR!! Invalid character in WIDTH_STATE %c %d\n",
		__func__, c, c);
	return FORMAT_ERROR_STATE;
}

static enum format_state
get_next_state_in_flag(char c)
{
	if (c == '.')
		return FORMAT_PRECISION_STATE;
	else if (IS_MATCH(c, format_flag_chars))
		return FORMAT_FLAG_STATE;
	else if (IS_NUMERICAL_NOT_ZERO(c))
		return FORMAT_WIDTH_STATE_CONFIRMED;

	diag_printf_wrapper("%s: ERROR!!\n", __func__);
	return FORMAT_ERROR_STATE;
}

static enum format_state
get_next_state_in_start(char c)
{
	if (IS_NUMERICAL_NOT_ZERO(c))
		return FORMAT_WIDTH_STATE;
	else if (c == '.')
		return FORMAT_PRECISION_STATE;
	else if (IS_MATCH(c, format_flag_chars))
		return FORMAT_FLAG_STATE;
	diag_printf_wrapper("%s: ERROR!!\n", __func__);
	return FORMAT_ERROR_STATE;
}

static enum format_state
get_next_state_in_parameter(char c)
{
	if (IS_NUMERICAL_NOT_ZERO(c))
		return FORMAT_WIDTH_STATE_CONFIRMED;
	else if (IS_MATCH(c, format_flag_chars))
		return FORMAT_FLAG_STATE;
	else if (c == '.')
		return FORMAT_PRECISION_STATE;

	diag_printf_wrapper("%s: ERROR!!\n", __func__);
	return FORMAT_ERROR_STATE;
}

static enum format_state
get_next_state(enum format_state cur_state, char c)
{
	if ((cur_state == FORMAT_NON_STATE) ||
	    (cur_state == FORMAT_END_STATE)) {
		if (c == '%')
			return FORMAT_START_STATE;
		else
			return FORMAT_NON_STATE;
	}

	if (IS_MATCH(c, format_type_chars))
		return FORMAT_END_STATE;
	else if (IS_MATCH(c, format_len_chars))
		return FORMAT_LENGTH_STATE;

	switch (cur_state) {
	case FORMAT_PRECISION_STATE:
		return get_next_state_in_precision(cur_state, c);
	case FORMAT_WIDTH_STATE:
	case FORMAT_WIDTH_STATE_CONFIRMED:
		return get_next_state_in_width(cur_state, c);
	case FORMAT_FLAG_STATE:
		return get_next_state_in_flag(c);
	case FORMAT_START_STATE:
		return get_next_state_in_start(c);
	case FORMAT_PARAMETER_STATE:
		return get_next_state_in_parameter(c);
	default:
		break;
	}
	diag_printf_wrapper(
		"%s: ERROR!! Invalid character %c for current state %d\n",
		__func__, c, cur_state);
	return FORMAT_ERROR_STATE;

}

static int parse_formatted_string(char *msg_str, int msg_len,
				  int *num_arguments)
{
	int cnt;
	enum format_state state = FORMAT_NON_STATE;
#define MAX_FORMAT_SPECIFIER_LEN 10
	*num_arguments = 0;

	for (cnt = 0; cnt < msg_len; cnt++) {
		state = get_next_state(state, msg_str[cnt]);
		if (state == FORMAT_END_STATE) {
			if (msg_str[cnt] != '%')
				/*
				 * '%' is also a format-type character
				 * %% will print '%' in the printf family.
				 *
				 * hence, we should make a distinction whether
				 * this is an argument or simply a '%%' print
				 */
				*num_arguments = (*num_arguments) +  1;
		} else if (state == FORMAT_ERROR_STATE) {
			diag_printf_wrapper("%s:%d ERROR %s!\n",
					    __func__, __LINE__, msg_str);
			return -1;
		}
	}
	if ((state != FORMAT_NON_STATE) && (state != FORMAT_END_STATE)) {
		diag_printf_wrapper("%s:%d ERROR! %d %s\n",
				    __func__, __LINE__, state, msg_str);
		return -1;
	}
	return 0;
}


/*
 * Fill block of log and process log data according to HDLC protocol
 * Every log ends with control charater 0x7E
 * According to HDLC protocol, User data which contains 0x7E is resolved using
 * an escape sequence which converts 7E to 7D-5E [with 7D being the escape
 * character]. If 7D is used in the data stream it again is converted
 * into 7D-5D
 */
static char *fill_block(char *input_buf, char *output_buf, int input_length,
			int output_buf_length, int *processed_len)
{
	char *input_end = input_buf + input_length;
	char *output_end = output_buf + output_buf_length;

	if (!(input_buf && output_buf && processed_len)) {
		diag_printf_wrapper("ERROR!! NULL pointer passed %p %p %p\n",
				input_buf, output_buf, processed_len);
		return (input_buf + input_length);
	}
	*processed_len = 0;

	/*
	 * First read the input buffer until the number of bytes read becomes
	 * equal the size of the output buffer
	 */
	while ((input_buf < input_end) && (output_buf < output_end)) {

		if (input_buf[0] == CONTROL_CHAR)
			return (input_buf+1);

		if ((input_buf[0] == ESC_CHAR) &&
				((input_buf + 1) < input_end)) {
			/*
			 * ESC_CHAR is a mechanism where you can put both
			 * ESC_CHAR and CONTROL_CHAR within the data stream,
			 * i.e.,when these bytes are part of the actual data
			 * and not block-end indicators.
			 */

			output_buf[0] = (input_buf[0] & 0xF0) |
						 (input_buf[1] & 0x0F);
			/*
			 * Skip an additional byte in case of ESC_CHAR
			 */
			input_buf++;
		} else if (input_buf[0] == ESC_CHAR) {
			/*
			 * This is an error condition. We encounter a ESC_CHAR
			 * and then immediately the buffer ends
			 */
			*processed_len = 0;
			diag_printf_wrapper(
				"ERROR!! No byte found after ESC_CHAR\n");
			return input_buf+1;
		} else
			/*
			 * Most-likely normal case
			 */
			output_buf[0] = input_buf[0];

		output_buf++;
		input_buf++;
		*processed_len = *processed_len + 1;
	}

	if ((input_buf < input_end) && (output_buf >= output_end)) {
		/*
		 * This is an error condition, i.e.,
		 * output buffer doesn't have enough space to fill the block
		 * and more data is available in the input stream.
		 * Mark the processed_len as 0, so that the caller of this
		 * function can recognize the error and does not proceed with
		 * what ever is available in the output buffer
		 */

		diag_printf_wrapper(
		"ERROR!! Not enough bytes available in the output buffer\n");
		*processed_len = 0;
	}

	/*
	 * Now, skip to the end of the block if there are more bytes
	 * than available in output buffer
	 */
	while (input_buf < input_end) {

		if (input_buf[0] == CONTROL_CHAR)
			return (input_buf+1);

		if (input_buf[0] == ESC_CHAR)
			input_buf++;

		input_buf++;
	}
	return input_buf;
}

void process_corediag_fw_msg(unsigned char *ptr, int len)
{
	char *data;
	char processed_data[BUF_SIZ*2] = {0};
	int i=0;
	char msgBuf[BUF_SIZ*2] = {0};
	uint32_t msgArg[DIAG_PRINT_MAX_ARG_NUM] = {0};
	int processed_len = 0;
	char *processed_data_ptr;
	int skip_len;
	int num_args_found_in_msg;

	data = (char *)ptr;
	while (len > 0) {
		msg_ext_type *msg_ext;

		processed_len = 0;
		data = fill_block(data, processed_data, len,
				  sizeof(processed_data), &processed_len);
		len -= (data-(char *)ptr);
		ptr = (unsigned char *)data;

		/*
		 * processed_data_ptr is a tracker pointer to make sure
		 * we don't go beyond the valid bytes filled in processed_data
		 */
		processed_data_ptr = processed_data;
		msg_ext = (msg_ext_type *)processed_data;

		skip_len = (((uint8_t *)msg_ext->args) - ((uint8_t *)msg_ext));
		processed_data_ptr += skip_len;
		if (processed_data_ptr > (processed_data+processed_len)) {
			diag_printf_wrapper("ERROR!! Not enough bytes "
					    "to be of type msg_ext_type\n");
			break;
		}

		/* Let's test the decoded string here */
		switch (processed_data[0]) {
		case DIAG_EXT_MSG_F:
		{
			char *msg_str;
			char *file_str;
			int null_termination_err;

			if (msg_ext->hdr.num_args > DIAG_PRINT_MAX_ARG_NUM) {
				diag_printf_wrapper("ERROR!! More arguments "
						    "indicated by the header "
						    "than supported\n");
				break;
			}

			/*
			 * Skip over the argument array in the message
			 * by as many words as indicated in the header
			 */
			skip_len = (msg_ext->hdr.num_args * sizeof(unsigned));
			processed_data_ptr += skip_len;
			if (processed_data_ptr >=
			    (processed_data+processed_len)) {
				diag_printf_wrapper("ERROR!! Not enough "
						    "arguments in the message "
						    "as in the header\n");
				break;
			}

			/*
			 * msg_str points to the actual message which
			 * may include format specifiers
			 */
			msg_str = processed_data_ptr;

			/*
			 * Make sure that message string is properly
			 * NULL-terminated
			 */
			null_termination_err = 0;
			while (*processed_data_ptr != '\0') {
				processed_data_ptr++;
				if (processed_data_ptr >=
					(processed_data+processed_len)) {
					null_termination_err = 1;
					break;
				}
			}
			if (null_termination_err) {
				diag_printf_wrapper("ERROR!! No NULL character"
						    " in the message part\n");
				break;
			}

			/*
			 * Now we get hold of the beginning of file name string
			 * But before that, make sure we haven't encountered
			 * end of the buffer
			 */
			processed_data_ptr++;
			if (processed_data_ptr >=
			    (processed_data+processed_len)) {
				diag_printf_wrapper("ERROR!! Buffer ends "
						    "immediately after message"
						    " string. No Filename "
						    "present\n");
				break;
			}
			file_str = processed_data_ptr;


			/*
			 * Now make sure file name string is NULL-terminated
			 */
			null_termination_err = 0;
			while (*processed_data_ptr != '\0') {
				processed_data_ptr++;
				if (processed_data_ptr >=
				    (processed_data+processed_len)) {
					null_termination_err = 1;
					break;
				}
			}
			if (null_termination_err) {
				diag_printf_wrapper("ERROR!! No NULL character "
						    "in the file-name "
						    "string\n");
				break;
			}

			if (parse_formatted_string(msg_str, file_str-msg_str,
						&num_args_found_in_msg) < 0) {
				break;
			} else if (num_args_found_in_msg >
				   DIAG_PRINT_MAX_ARG_NUM) {
				diag_printf_wrapper("ERROR!! TOO MANY "
						    "ARGUMENTS %d in %s\n",
						    num_args_found_in_msg,
						    msg_str);
				break;
			} else if (num_args_found_in_msg !=
				   msg_ext->hdr.num_args) {
				diag_printf_wrapper("WARNING!! argument number"
						    " ismatch msg %d header %d"
						    " STRING %s\n",
						    num_args_found_in_msg,
						    msg_ext->hdr.num_args,
						    msg_str);
			}

			if (msg_ext->hdr.num_args != 0) {
				memset(msgBuf, 0, sizeof(msgBuf));
				//F3 MSGs do not support string parameter
				//only support upto 9 integers
				for (i = 0; i < msg_ext->hdr.num_args; i++)
					msgArg[i] = msg_ext->args[i];
				snprintf(msgBuf, sizeof(msgBuf), msg_str,
					 msgArg[0], msgArg[1], msgArg[2],
					 msgArg[3], msgArg[4], msgArg[5],
					 msgArg[6], msgArg[7], msgArg[8]);
				if (optionflag & CONSOLE_FLAG) {
					android_printf("EXTMSG [0x%08x %08x] "
							"SSID[%d] %s "
							"Line[%04d]: %s\n",
							msg_ext->hdr.ts_hi,
							msg_ext->hdr.ts_lo,
							msg_ext->desc.ss_id,
							file_str,
							msg_ext->desc.line,
							msgBuf);

				}
				if (optionflag & SYSLOG_FLAG) {

					syslog(LOG_INFO,
					       "EXTMSG [0x%08x %08x] SSID[%d] "
					       "%s Line[%04d]: %s\n",
						msg_ext->hdr.ts_hi,
						msg_ext->hdr.ts_lo,
						msg_ext->desc.ss_id,
						file_str,
						msg_ext->desc.line, msgBuf);
				}
			} else {
				if (optionflag & CONSOLE_FLAG) {
					android_printf("EXTMSG [0x%08x %08x] "
							"SSID[%d] %s Line[%04d]"
							": %s\n",
							msg_ext->hdr.ts_hi,
							msg_ext->hdr.ts_lo,
							msg_ext->desc.ss_id,
							file_str,
							msg_ext->desc.line,
							msg_str);
				}

				if (optionflag & SYSLOG_FLAG) {
					syslog(LOG_INFO,
					       "EXTMSG [0x%08x %08x] SSID[%d] "
					       "%s Line[%04d]: %s\n",
						msg_ext->hdr.ts_hi,
						msg_ext->hdr.ts_lo,
						msg_ext->desc.ss_id, file_str,
						msg_ext->desc.line, msg_str);
				}
			}
			break;
		}
		default:
			syslog(LOG_INFO, "Unsuport type %2x\n",
			       processed_data[0]);
		}
	}
}
