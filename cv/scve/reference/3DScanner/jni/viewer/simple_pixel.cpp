"precision highp float;\n"
"in vec2 v_texCoord;                                 \n"
"in vec4 v_normal;                                 \n"
"in vec4 v_position;\n"
"layout(location = 0) out vec4 outColor;             \n"
"uniform sampler2D s_texture;                        \n"
"uniform vec4      cameraPos;\n"
"uniform int       doGrey;\n"
"\n"
"\n"
"void main()                                         \n"
"{                                                   \n"
"    vec4 camera = vec4(0., 0., 1., 0.);\n"
"    vec4 eyeDiff, eyeVec;\n"
"    float falloff;\n"
"    float minimumBrightness = .3;\n"
"\n"
"    eyeDiff = cameraPos - v_position;\n"
"    eyeDiff.w = 0.;\n"
"    eyeVec = normalize(eyeDiff);\n"
"    outColor = texture( s_texture, v_texCoord ); //for color \n"
"\n"
"    falloff = 1.f;\n"
"    if (doGrey == 1)\n"
"    {\n"
"       outColor = vec4(.5, .5, .5, 1.); // for flat grey\n"
"       falloff = max(dot (eyeVec.rgb, v_normal.rgb), minimumBrightness);\n"
"    }\n"
"\n"
"    outColor = outColor * falloff;\n"
"}                                                   \n"
; 
