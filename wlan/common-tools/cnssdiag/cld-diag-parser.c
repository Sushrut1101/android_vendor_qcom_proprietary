/*
 * macros to safely extract 8, 16, 32, or 64-bit values from byte buffer
 */
#define GET_8(v, msg, msg_len) do {         \
	if (msg_len < sizeof(uint8_t)) {    \
		goto msg_error;             \
	}                                   \
	v = *msg;                           \
	msg += sizeof(uint8_t);             \
	msg_len -= sizeof(uint8_t);         \
} while (0)

#define _GET_LE16(a) ( \
	(((uint16_t)(a)[1]) << 8) | \
	 ((uint16_t)(a)[0]))
#define GET_LE16(v, msg, msg_len) do {      \
	if (msg_len < sizeof(uint16_t)) {   \
		goto msg_error;             \
	}                                   \
	v = _GET_LE16(msg);                 \
	msg += sizeof(uint16_t);            \
	msg_len -= sizeof(uint16_t);        \
} while (0)

#define _GET_LE32(a) ( \
	(((uint32_t)(a)[3]) << 24) | \
	(((uint32_t)(a)[2]) << 16) | \
	(((uint32_t)(a)[1]) << 8)  | \
	((uint32_t)(a)[0]))
#define GET_LE32(v, msg, msg_len) do {      \
	if (msg_len < sizeof(uint32_t)) {   \
		goto msg_error;             \
	}                                   \
	v = _GET_LE32(msg);                 \
	msg += sizeof(uint32_t);            \
	msg_len -= sizeof(uint32_t);        \
} while (0)

#define _GET_LE64(a) ( \
	(((uint64_t)(a)[7]) << 56) | \
	(((uint64_t)(a)[6]) << 48) | \
	(((uint64_t)(a)[5]) << 40) | \
	(((uint64_t)(a)[4]) << 32) | \
	(((uint64_t)(a)[3]) << 24) | \
	(((uint64_t)(a)[2]) << 16) | \
	(((uint64_t)(a)[1]) << 8) | \
	((uint64_t)(a)[0]))
#define GET_LE64(v, msg, msg_len) do {      \
	if (msg_len < sizeof(uint64_t)) {   \
		goto msg_error;             \
	}                                   \
	v = _GET_LE64(msg);                 \
	msg += sizeof(uint64_t);            \
	msg_len -= sizeof(uint64_t);        \
} while (0)

