void diag_printf_wrapper(const char *msg, ...)
{
	va_list valist;
	char print_buf_array[512];
	char *print_buf;
	int bytes_needed;

	va_start(valist, msg);
	bytes_needed = vsnprintf(NULL, 0, msg, valist);
	bytes_needed += 1; /* For null character */
	va_end(valist);

	/*
	 * If the required bytes is less use stack, otherwise
	 * allocate from malloc
	 */
	if (bytes_needed > sizeof(print_buf_array)) {
		print_buf = malloc(bytes_needed);
		if (print_buf == NULL) {
			diag_printf(
			"ERROR!! malloc failed in diag_printf_wrapper\n",
			-1, DBGLOG_ERR, optionflag, 0, NULL, 0);
			return;
		}
	} else
		print_buf = print_buf_array;

	va_start(valist, msg);
	vsnprintf(print_buf, bytes_needed, msg, valist);
	va_end(valist);
	diag_printf(print_buf, -1, DBGLOG_ERR, optionflag, 0, NULL, 0);
	if (print_buf != print_buf_array)
		free(print_buf);
}


